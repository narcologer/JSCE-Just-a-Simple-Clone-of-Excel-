    void dothesave() {
        dtm=(DefaultTableModel)jTable1.getModel();
        String S="";
        for (int i=0; i<dtm.getColumnCount(); i++)
        {
            S+=dtm.getColumnName(i)+"_";
        }
        S+='\n';
        for (int i=0; i<dtm.getRowCount(); i++)
        {
            for (int j=0; j<dtm.getColumnCount(); j++)
            {
                S+=dtm.getValueAt(i, j)+"_";
            }
            S+='\n';
        }
        JFileChooser fc = new JFileChooser();
            FileNameExtensionFilter filter = new FileNameExtensionFilter("Database file","dat");
            fc.setFileFilter(filter);
            fc.setFileSelectionMode(JFileChooser.FILES_ONLY);
            if ( fc.showSaveDialog(null) == JFileChooser.APPROVE_OPTION ) 
            {
                try 
                {
                    FileWriter fw;
                    File fi = fc.getSelectedFile();
                    String ext=fi.getName();
                    if (!ext.endsWith(".dat"))
                    {
                        fw = new FileWriter(fc.getSelectedFile()+".dat");
                    }
                    else
                    {
                        fw = new FileWriter(fc.getSelectedFile());
                    }
                    fw.write(S);
                    fw.flush();
                }
            catch ( IOException e ) 
                {
                    System.out.println(e.getMessage());
                }
        }
    }
    
