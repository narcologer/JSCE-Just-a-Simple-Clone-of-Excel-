    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        dtm=(DefaultTableModel)jTable1.getModel();
        if (dtm.getColumnCount()!=0)
        {
            int opcion = JOptionPane.showConfirmDialog(null, "Хотите сохранить таблицу?", "Подтверждение", JOptionPane.YES_NO_OPTION);

            if (opcion == 0) { //The ISSUE is here
                dothesave();
            }
        }      
            JFileChooser fc = new JFileChooser();
            FileNameExtensionFilter filter = new FileNameExtensionFilter("Database file","dat","*.*");
            fc.setFileFilter(filter);
            fc.addChoosableFileFilter(filter);
            fc.setFileSelectionMode(JFileChooser.FILES_ONLY);
            if ( fc.showOpenDialog(null) == JFileChooser.APPROVE_OPTION ) 
            {
               try  
               {
                   File fi = fc.getSelectedFile();
                   BufferedReader fr = new BufferedReader(new FileReader(fi.getPath()));
                   String[] ss2;
                   String ss1;
                   data.cells.clear();
                   boolean first_line=true;
                    while ((ss1 = fr.readLine()) != null) 
                    {
                        ss2=ss1.split("_");
                        if (first_line==false)
                        {
                            data.columns=ss2.length;
                            data.cells.add(ss2);
                        }
                        else
                        {
                            data.columns_names=ss1.split("_");
                            first_line=false;
                        }
                    }
                    fr.close();
               }
            catch ( IOException e ) 
                {
                    System.out.println(e.getMessage());
                }
            }
        String[][] array = new String[data.cells.size()][];
        for (int i = 0; i < data.cells.size(); i++) {
        String[] row = data.cells.get(i);
        array[i] = row;
        }
            jTable1.setModel(new javax.swing.table.DefaultTableModel(array,data.columns_names));
    }//GEN-LAST:event_jButton4ActionPerformed

